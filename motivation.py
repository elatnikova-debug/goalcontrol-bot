def get_streak_message(streak: int) -> str:
    """Вернуть сообщение о стрике, если он достиг особой отметки."""
    if streak in STREAK_MESSAGES:
        return STREAK_MESSAGES[streak]
    if streak > 0 and streak % 10 == 0:
        return f"🔥 {streak} дней подряд! Ты не останавливаешься!"
    return ""


def format_progress_bar(completed: int, total: int, length: int = 10) -> str:
    """Создать визуальный прогресс-бар."""
    if total == 0:
        return "░" * length
    filled = int(length * completed / total)
    bar = "█" * filled + "░" * (length - filled)
    pct = int(100 * completed / total)
    return f"{bar} {pct}%"
